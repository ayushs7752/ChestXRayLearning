def best_validation_accuracy(data_size, layers):
    listing = accuracies[data_size][layers]
    return max(listing) - val_maj_acc[data_size][layers]

def pos_options():
    for s in size:
        for l in layers:
            acc = best_validation_accuracy(s, l)
            if acc > 0.06:
                print("Acc:" , acc , "for size" , s , "and layers" , l)
